package org.igrok_net.engine.ui.events;

/**
 * Contains mouse buttons to decode codes returned in mousepress event
 * @author Oleg Golovchenko
 * @version 0.0.1
 */
public class MousePress {

    /**
     * Left mouse button
     */
    public static final long LMB = 1;

    /**
     * Middle mouse button
     */
    public static final long MMB = 2;

    /**
     * Right mouse button
     */
    public static final long RMB = 3;

    /**
     * Mouse wheel up
     */
    public static final long MMBUP = 4;

    /**
     * Mouse wheel down
     */
    public static final long MMBDWN = 5;
}
